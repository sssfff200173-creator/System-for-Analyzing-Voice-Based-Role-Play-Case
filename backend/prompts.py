from typing import List

ALL_CRITERIA = ["filler_words", "rudeness", "politeness", "coherence"]

CRITERIA_DESCRIPTIONS = {
    "filler_words": (
        'Слова-паразиты: "ну", "типа", "короче", "вот", "это самое", "как бы", "значит", "собственно", '
        '"ладно", "в общем-то", "на самом деле" (в начале фраз), "просто" (в значении вводного слова) и похожие. '
        "Считай КАЖДОЕ вхождение в ответах кандидата. Если больше 2 — основание для отрицательного вердикта."
    ),
    "rudeness": (
        "Грубость — это ЛЮБОЕ из следующего в словах кандидата:\n"
        "   • Прямые оскорбления, агрессия, повышение тона.\n"
        "   • Пассивная агрессия: пренебрежительные обращения («девушка», «женщина» вместо имени в грубом контексте), "
        "сарказм («ну конечно», «как интересно», «подумать только»), демонстративное недоверие («вы серьёзно?», «и это всё?»).\n"
        "   • Фразы, подчёркивающие некомпетентность клиента: «я же сказала», «я уже говорила», «как я уже объясняла», "
        "«вы слышите меня?», «я повторяю».\n"
        "   • Имитация спокойствия при явном раздражении: нарочито медленные, 'разжёванные' ответы, "
        "риторические вопросы ('что тут непонятного?', 'как иначе вам объяснить?').\n"
        "   • Игнорирование эмоций клиента: переход сразу к инструкциям без выражения сочувствия "
        "в ситуации, когда клиент явно расстроен.\n"
        "ВАЖНО: оценивай только слова кандидата, НЕ клиента. Любая грубость — основание для «Не рекомендуется»."
    ),
    "politeness": (
        "Вежливость в словах кандидата: слова извинения («простите», «приношу извинения», «к сожалению»), "
        "эмпатия («я вас понимаю», «понимаю ваше расстройство», «вы правы, ситуация неприятная»), "
        "уважительное обращение на «вы», благодарность («спасибо, что обратились»), "
        "готовность помочь («давайте разберёмся», «я помогу вам решить этот вопрос»). "
        "Считай КАЖДЫЙ отдельный проявленный маркер вежливости."
    ),
    "coherence": (
        "Связность речи — оцени целым числом от 0 до 10, насколько ответы кандидата:\n"
        "   • Соответствуют конкретному вопросу/жалобе клиента (кандидат отвечает по существу, не уходит от темы).\n"
        "   • Логически выстроены — предложения и мысли следуют друг из друга, нет хаотичных перепрыгиваний.\n"
        "   • Понятны слушателю — нет обрывистых незаконченных мыслей, размытых или пустых формулировок.\n"
        "   • Фразы согласованы между собой — нет внутренних противоречий, кандидат не противоречит сам себе.\n"
        "Шкала: 0-4 = речь бессвязна или ответ не по теме; 5-7 = частично логично, но есть провалы; "
        "8-10 = чёткая, структурированная, полностью релевантная речь.\n"
        "В поле coherence_issues укажи конкретные примеры нарушений связности (цитаты или описание), "
        "или оставь пустой массив если речь полностью связна."
    ),
}

CRITERIA_JSON_KEYS = {
    "filler_words": ("filler_words_count", "filler_words_examples"),
    "rudeness": ("rudeness_count", "rudeness_examples"),
    "politeness": ("politeness_count", "politeness_examples"),
    "coherence": ("coherence_score", "coherence_issues"),
}

VERDICT_RULES = {
    "filler_words": "Если слов-паразитов более 2 — вердикт «Не рекомендуется».",
    "rudeness": "Если найдена ХОТЯ БЫ ОДНА грубость (включая пассивную агрессию) — вердикт «Не рекомендуется».",
    "coherence": "Если coherence_score меньше 6 — вердикт «Не рекомендуется».",
}


def build_system_prompt(selected_criteria: List[str]) -> str:
    if not selected_criteria:
        selected_criteria = ALL_CRITERIA

    criteria_lines = []
    for i, key in enumerate(selected_criteria, start=1):
        desc = CRITERIA_DESCRIPTIONS.get(key, "")
        if desc:
            criteria_lines.append(f"{i}. {desc}")

    verdict_rules = []
    for key in selected_criteria:
        rule = VERDICT_RULES.get(key)
        if rule:
            verdict_rules.append(rule)

    json_fields = []
    for key in selected_criteria:
        count_key, examples_key = CRITERIA_JSON_KEYS[key]
        json_fields.append(f'    "{count_key}": <число>,')
        json_fields.append(f'    "{examples_key}": [<список строк или пустой массив>],')

    criteria_block = "\n".join(criteria_lines)
    rules_block = " ".join(verdict_rules) if verdict_rules else "Оценивай строго по выбранным критериям."
    fields_block = "\n".join(json_fields).rstrip(",")

    return f"""Ты — строгий HR-эксперт по оценке операторов контакт-центра.
Проанализируй ПОЛНЫЙ диалог: реплики Клиента приведены для контекста — оценивай ТОЛЬКО ответы Кандидата.
Верни СТРОГО только валидный JSON без markdown-блоков, без пояснений, без текста до или после JSON.

Критерии оценки (только для ответов Кандидата):
{criteria_block}

Правила вердикта:
{rules_block}
Если ни одного нарушения не выявлено — вердикт «Рекомендуется».

Верни JSON строго в таком формате:
{{
  "verdict": "Рекомендуется" | "Не рекомендуется",
  "markers": {{
{fields_block}
  }},
  "quotes": [<дословные цитаты из ответов Кандидата, иллюстрирующие найденные маркеры; [] если нет>],
  "comment": "<краткий HR-комментарий в 1-2 предложения>"
}}

Если по критерию ничего не найдено — счётчик строго 0, массив [].
Не добавляй никакого текста за пределами JSON.
"""


def build_evaluation_prompt(dialog: List[dict]) -> str:
    lines = []
    for turn in dialog:
        role = turn.get("role", "")
        text = turn.get("text", "")
        lines.append(f"[{role}]: {text}")
    return "Диалог для анализа:\n" + "\n".join(lines)
