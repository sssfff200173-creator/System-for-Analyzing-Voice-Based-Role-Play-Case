def build_evaluation_prompt(dialogue: list[dict]) -> str:
    candidate_lines = []
    dialogue_text = ""
    for turn in dialogue:
        role_label = "Клиент" if turn["role"] == "client" else "Кандидат"
        dialogue_text += f"{role_label}: {turn['text']}\n"
        if turn["role"] == "candidate":
            candidate_lines.append(turn["text"])

    candidate_text = "\n".join(f"- {line}" for line in candidate_lines)

    return f"""Ты строгий HR-эксперт контакт-центра. Оцени ТОЛЬКО реплики Кандидата в диалоге ниже.

ПОЛНЫЙ ДИАЛОГ (для контекста):
{dialogue_text}

РЕПЛИКИ КАНДИДАТА (анализируй только их):
{candidate_text}

ПРАВИЛА АНАЛИЗА:
1. Слова-паразиты: "ну", "типа", "короче", "эм", "ааа", "блин", "вот", "как бы", "в общем", "значит", "собственно". Более 2 таких слов → вердикт "Не рекомендуется".
2. Грубость / негатив: пренебрежение, повышенный тон, обвинение клиента, нетактичные формулировки → автоматически "Не рекомендуется".
3. Вежливость: явные извинения ("извините", "прошу прощения"), благодарности ("спасибо"), предложение помощи ("могу помочь"), обращение по имени.

СТРОГИЕ ПРАВИЛА ЦИТИРОВАНИЯ — нарушение недопустимо:
- Цитаты должны быть ДОСЛОВНЫМИ фрагментами из реплик Кандидата выше. Не перефразируй, не обобщай.
- ЗАПРЕЩЕНО придумывать слова или фразы, которых нет в тексте кандидата.
- Если маркер не найден — верни пустой массив [] для этой категории.
- Каждая цитата — отдельная строка или фраза, а не весь абзац целиком.

Верни СТРОГО JSON без каких-либо пояснений, markdown-разметки или дополнительного текста:
{{
  "verdict": "Рекомендуется" или "Не рекомендуется",
  "fillerQuotes": ["<дословный фрагмент из реплики Кандидата>"],
  "rudenessQuotes": ["<дословный фрагмент из реплики Кандидата>"],
  "politenessQuotes": ["<дословный фрагмент из реплики Кандидата>"]
}}"""
