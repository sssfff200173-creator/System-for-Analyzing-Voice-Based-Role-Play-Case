def build_evaluation_prompt(dialogue: list[dict]) -> str:
    dialogue_text = ""
    for turn in dialogue:
        role_label = "Клиент" if turn["role"] == "client" else "Кандидат"
        dialogue_text += f"{role_label}: {turn['text']}\n"

    return f"""Ты строгий HR-эксперт контакт-центра. Оцени диалог кандидата на должность оператора по следующим критериям:

ДИАЛОГ:
{dialogue_text}

КРИТЕРИИ ОЦЕНКИ:
1. Слова-паразиты: "ну", "типа", "короче", "эм", "ааа", "блин", "вот", "как бы" — если более 2 таких слов, вердикт "Не рекомендуется"
2. Грубость или нецензурная лексика — автоматически "Не рекомендуется"
3. Вежливость и профессионализм в общении с раздражённым клиентом
4. Конкретность и полезность ответов

Верни СТРОГО JSON без каких-либо пояснений, markdown или дополнительного текста:
{{
  "verdict": "Рекомендуется" или "Не рекомендуется",
  "markers": {{
    "fillerWordCount": <число слов-паразитов>,
    "politenessMarkers": <число вежливых фраз>
  }},
  "quotes": [<точные цитаты из ответов кандидата, иллюстрирующие маркеры>]
}}"""
